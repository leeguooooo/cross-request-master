function findDocsSyncHome(startDir: string): string | null {
  let current = path.resolve(startDir);
  while (true) {
    const candidate = path.join(current, ".yapi");
    if (fs.existsSync(candidate) && fs.statSync(candidate).isDirectory()) {
      return candidate;
    }
    const parent = path.dirname(current);
    if (parent === current) return null;
    current = parent;
  }
}

function resolveDocsSyncHome(startDir: string, ensure: boolean): string | null {
  const found = findDocsSyncHome(startDir);
  if (found) return found;
  if (!ensure) return null;
  const home = path.join(path.resolve(startDir), ".yapi");
  fs.mkdirSync(home, { recursive: true });
  ensureDocsSyncReadme(home);
  return home;
}

function globalYapiHomeDir(): string {
  return path.resolve(process.env.YAPI_HOME || path.join(require("os").homedir(), ".yapi"));
}

function normalizeComparablePath(targetPath: string): string {
  try {
    return fs.realpathSync.native(targetPath);
  } catch {
    return path.resolve(targetPath);
  }
}

function isGlobalDocsSyncHome(homeDir: string): boolean {
  return normalizeComparablePath(homeDir) === normalizeComparablePath(globalYapiHomeDir());
}

