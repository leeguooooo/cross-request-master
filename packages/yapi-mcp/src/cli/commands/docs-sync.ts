const SCAN_EXCLUDE_DIRS = new Set([
  "node_modules",
  ".git",
  ".yapi",
  "dist",
  "build",
  ".next",
  ".astro",
  "coverage",
  "out",
  ".turbo",
  ".cache",
]);

function findBindingDirCandidates(
  scanRoot: string,
  expectedFiles: string[],
  maxDepth = 4,
): { relativeDir: string; matchCount: number; total: number }[] {
  if (!expectedFiles.length) return [];
  const total = expectedFiles.length;
  const expectedSet = new Set(expectedFiles);
  const matches: { relativeDir: string; matchCount: number; total: number }[] = [];

  const walk = (currentDir: string, depth: number): void => {
    if (depth > maxDepth) return;
    let entries: fs.Dirent[];
    try {
      entries = fs.readdirSync(currentDir, { withFileTypes: true });
    } catch {
      return;
    }
    let matchCount = 0;
    for (const entry of entries) {
      if (entry.isFile() && expectedSet.has(entry.name)) {
        matchCount++;
      }
    }
    if (matchCount > 0) {
      const rel = path.relative(scanRoot, currentDir);
      if (rel) {
        matches.push({ relativeDir: rel, matchCount, total });
      }
    }
    for (const entry of entries) {
      if (!entry.isDirectory()) continue;
      if (SCAN_EXCLUDE_DIRS.has(entry.name)) continue;
      if (entry.name.startsWith(".")) continue;
      walk(path.join(currentDir, entry.name), depth + 1);
    }
  };

  walk(scanRoot, 0);
  return matches
    .filter((item) => item.matchCount * 2 >= item.total)
    .sort((a, b) => b.matchCount - a.matchCount || a.relativeDir.localeCompare(b.relativeDir));
}

function buildMissingBindingDirMessage(
  bindingName: string,
  bindingDir: string,
  binding: DocsSyncBinding,
  scanRoot: string,
): string {
  const expectedFiles = binding.files ? Object.keys(binding.files).sort() : [];
  const candidates = findBindingDirCandidates(scanRoot, expectedFiles);
  const lines: string[] = [];
  lines.push(`binding '${bindingName}' dir=${bindingDir} 在当前仓库找不到。`);
  if (candidates.length > 0) {
    const top = candidates[0];
    lines.push(`候选目录（命中 ${top.matchCount}/${top.total} 文件）：`);
    lines.push(`  ${top.relativeDir}`);
    if (candidates.length > 1) {
      const others = candidates
        .slice(1, 4)
        .map((c) => `  ${c.relativeDir} (命中 ${c.matchCount}/${c.total})`);
      lines.push("其它候选：");
      lines.push(...others);
    }
    lines.push(
      `hint: yapi docs-sync bind update --name ${bindingName} --dir ${top.relativeDir}`,
    );
  } else {
    lines.push(
      `hint: yapi docs-sync bind update --name ${bindingName} --dir <new-relative-path>`,
    );
  }
  return lines.join("\n");
}

function getBindingBaseDir(homeDir: string, rootDir: string, cwd: string): {
  baseDir: string;
  gitRoot: string | null;
  usedGitRoot: boolean;
} {
  if (!isGlobalDocsSyncHome(homeDir)) {
    return { baseDir: rootDir, gitRoot: findGitRoot(cwd), usedGitRoot: false };
  }
  const gitRoot = findGitRoot(cwd);
  if (gitRoot) {
    return { baseDir: gitRoot, gitRoot, usedGitRoot: true };
  }
  return { baseDir: path.resolve(cwd), gitRoot: null, usedGitRoot: false };
}

function normalizeBindingDirForContext(
  homeDir: string,
  rootDir: string,
  cwd: string,
  bindingDir: string,
): string {
  const context = getBindingBaseDir(homeDir, rootDir, cwd);
  const resolved = path.isAbsolute(bindingDir)
    ? bindingDir
    : path.resolve(context.baseDir, bindingDir);
  const relative = path.relative(rootDir, resolved);
  if (!relative || relative === ".") return ".";
  if (relative.startsWith("..") || path.isAbsolute(relative)) return resolved;
  return relative;
}

function resolveBindingDirForContext(
  homeDir: string,
  rootDir: string,
  cwd: string,
  bindingDir: string,
): string {
  if (!bindingDir) return rootDir;
  if (path.isAbsolute(bindingDir)) return bindingDir;
  const direct = path.resolve(rootDir, bindingDir);
  if (!isGlobalDocsSyncHome(homeDir)) return direct;
  if (fs.existsSync(direct)) return direct;
  const { baseDir } = getBindingBaseDir(homeDir, rootDir, cwd);
  const contextual = path.resolve(baseDir, bindingDir);
  if (fs.existsSync(contextual)) return contextual;
  return direct;
}

function suggestDocsSyncDir(startDir: string): string | null {
  const candidates = ["docs", "doc", "documentation", "release-notes"];
  for (const candidate of candidates) {
    const candidatePath = path.resolve(startDir, candidate);
    if (fs.existsSync(candidatePath) && fs.statSync(candidatePath).isDirectory()) {
      const relative = path.relative(startDir, candidatePath);
      return relative && relative !== "." ? relative : candidate;
    }
  }
  return null;
}

