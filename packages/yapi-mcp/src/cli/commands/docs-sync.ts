function resolveInitialRenderMode(
  options: DocsSyncOptions,
  rememberedMode?: "classic" | "no-mermaid",
): DocsSyncRenderMode {
  if (options.noMermaid) return "no-mermaid";
  if (options.mermaidLook === "classic") return "classic";
  if (rememberedMode) return rememberedMode;
  return "default";
}

function resolveRetryModes(
  mode: DocsSyncRenderMode,
  hasMermaidBlocks: boolean,
): DocsSyncRenderMode[] {
  if (!hasMermaidBlocks) return [];
  if (mode === "default") return ["classic", "no-mermaid"];
  if (mode === "classic") return ["no-mermaid"];
  return [];
}


async function addInterface(
  title: string,
  apiPath: string,
  mapping: DocsSyncMapping,
  request: YapiRequest,
): Promise<number> {
  const projectId = Number(mapping.project_id || 0);
  const catId = Number(mapping.catid || 0);
  if (!projectId || !catId) {
    throw new Error("project_id and catid are required to create new docs");
  }
  let template: Record<string, any> = {};
  if (mapping.template_id) {
    const resp = await request("/api/interface/get", "GET", { id: mapping.template_id });
    if (resp?.errcode !== 0) {
      throw new Error(`interface get failed: ${resp?.errmsg || "unknown error"}`);
    }
    template = resp?.data || {};
  }
  const payload = buildAddPayload(template, title, apiPath, catId, projectId);
  const resp = await request("/api/interface/add", "POST", {}, payload);
  if (resp?.errcode !== 0) {
    throw new Error(`interface add failed: ${resp?.errmsg || "unknown error"}`);
  }
  const newId = resp?.data?._id;
  if (!newId) {
    throw new Error("interface add succeeded but missing id");
  }
  return Number(newId);
}

async function updateInterface(
  docId: number,
  title: string | undefined,
  markdownField: string,
  html: string,
  request: YapiRequest,
): Promise<void> {
  const payload = buildUpdatePayload(docId, title, markdownField, html);
  const resp = await request("/api/interface/up", "POST", {}, payload);
  if (resp?.errcode !== 0) {
    throw new Error(`interface up failed: ${resp?.errmsg || "unknown error"}`);
  }
}

async function syncDocsDir(
  dirPath: string,
  mapping: DocsSyncMapping,
  options: DocsSyncOptions,
  request: YapiRequest,
): Promise<{
  updated: number;
  created: number;
  skipped: number;
  previewOnly: number;
  files: Record<string, DocsSyncFileInfo>;
  previews: DocsSyncPreviewItem[];
}> {
  if (!mapping.files || typeof mapping.files !== "object") {
    mapping.files = {};
  }
  if (!mapping.file_hashes || typeof mapping.file_hashes !== "object") {
    mapping.file_hashes = {};
  }

  const envProjectId = process.env.YAPI_PROJECT_ID;
  const envCatId = process.env.YAPI_CATID;
  const envTemplateId = process.env.YAPI_TEMPLATE_ID;
  if (!mapping.project_id && envProjectId) mapping.project_id = Number(envProjectId);
  if (!mapping.catid && envCatId) mapping.catid = Number(envCatId);
  if (!mapping.template_id && envTemplateId) mapping.template_id = Number(envTemplateId);

  const hasTarget = Boolean(mapping.project_id && mapping.catid);
  if (!hasTarget && !options.dryRun) {
    throw new Error(
      "缺少 project_id/catid。请先绑定或配置：yapi docs-sync bind add --name <binding> --dir <path> --project-id <id> --catid <id>，或在目录下添加 .yapi.json，或设置环境变量 YAPI_PROJECT_ID/YAPI_CATID。"
    );
  }

  const { byPath, byTitle, byId } = hasTarget
    ? await listExistingInterfaces(Number(mapping.catid), request)
    : { byPath: {}, byTitle: {}, byId: {} };

  let updated = 0;
  let created = 0;
  let skipped = 0;
  let previewOnly = 0;
  const fileInfos: Record<string, DocsSyncFileInfo> = {};
  const previews: DocsSyncPreviewItem[] = [];
  const files = resolveSourceFiles(dirPath, mapping);
  for (const mdPath of files) {
    const stem = path.parse(mdPath).name;
    const relName = path.basename(mdPath);
    const apiPath = `/${stem}`;

    const doc = loadSourceDoc(mdPath, relName);
    const desiredTitle = doc.title || stem;

    let docId = mapping.files[relName];
    if (!docId) {
      docId = byPath[apiPath] || byTitle[desiredTitle] || byTitle[stem];
      if (docId) mapping.files[relName] = docId;
    }

    let action: DocsSyncPreviewAction = docId ? "update" : hasTarget ? "create" : "preview-only";

    if (!docId && hasTarget) {
      created += 1;
      if (!options.dryRun) {
        docId = await addInterface(desiredTitle, apiPath, mapping, request);
        mapping.files[relName] = docId;
      }
    }
    if (!docId && !hasTarget) {
      previewOnly += 1;
    }

    if (docId) {
      const resolvedPath = byId[String(docId)]?.path || apiPath;
      fileInfos[relName] = { docId: Number(docId), apiPath: resolvedPath };
    }

    const logPrefix = `[docs-sync:${relName}]`;
    const hasMermaidBlocks =
      doc.kind === "markdown" && /```mermaid\s*\r?\n/i.test(doc.raw);
    const rememberedMode =
      !options.noMermaid && options.mermaidLook === "handDrawn"
        ? mapping.file_render_modes?.[relName]
        : undefined;
    let appliedMode = resolveInitialRenderMode(options, rememberedMode);
    let effectiveOptions = resolveDocsSyncOptionsForMode(options, appliedMode);
    if (rememberedMode) {
      console.log(`${logPrefix} using remembered fallback: ${resolveRenderModeLabel(appliedMode)}.`);
    }

    let contentHash = buildDocsSyncHash(doc, effectiveOptions);
    const previousHash = mapping.file_hashes[relName];
    const currentTitle = docId ? byId[String(docId)]?.title : "";
    const titleToUpdate = !docId
      ? undefined
      : !currentTitle || currentTitle !== desiredTitle
        ? desiredTitle
        : undefined;
    const shouldSyncTitle = Boolean(titleToUpdate);
    const shouldSkipUnchanged =
      !options.force &&
      docId &&
      previousHash &&
      previousHash === contentHash &&
      !shouldSyncTitle;

    if (shouldSkipUnchanged && !options.dryRun) {
      skipped += 1;
      continue;
    }

    let { html, mermaidFailed, diagramFailed, diagramMetrics } = renderSourceDocToHtml(
      doc,
      effectiveOptions,
      logPrefix,
    );

    if (shouldSkipUnchanged) {
      action = "skip";
      skipped += 1;
    }

    const payloadObject =
      docId && action !== "create"
        ? buildUpdatePayload(docId, titleToUpdate, buildPayloadMarkdownField(doc), html)
        : buildAddPayload({}, desiredTitle, apiPath, Number(mapping.catid || 0), Number(mapping.project_id || 0));
    const preview: DocsSyncPreviewItem = {
      fileName: relName,
      action,
      markdownBytes: Buffer.byteLength(doc.raw, "utf8"),
      htmlBytes: Buffer.byteLength(html, "utf8"),
      payloadBytes: Buffer.byteLength(JSON.stringify(payloadObject), "utf8"),
      apiPath: docId ? fileInfos[relName]?.apiPath || apiPath : apiPath,
      docId: docId ? Number(docId) : undefined,
      largestMermaid: pickLargestMermaid(diagramMetrics),
    };
    previews.push(preview);

    if (!options.dryRun && docId && action !== "skip") {
      try {
        await updateInterface(docId, titleToUpdate, buildPayloadMarkdownField(doc), html, request);
      } catch (error) {
        if (error instanceof HttpStatusError && error.status === 413) {
          const attempts: DocsSyncRetryAttempt[] = [];
          let resolved = false;
          for (const retryMode of resolveRetryModes(appliedMode, hasMermaidBlocks)) {
            console.warn(
              `${logPrefix} 413 received, retrying with ${resolveRenderModeLabel(retryMode)} for this file.`,
            );
            const retryAttempt: DocsSyncRetryAttempt = { mode: retryMode };
            attempts.push(retryAttempt);
            try {
              const retryOptions = resolveDocsSyncOptionsForMode(options, retryMode);
              const retryResult = renderSourceDocToHtml(doc, retryOptions, logPrefix);
              const retryPayload = buildUpdatePayload(
                docId,
                titleToUpdate,
                buildPayloadMarkdownField(doc),
                retryResult.html,
              );
              retryAttempt.payloadBytes = Buffer.byteLength(JSON.stringify(retryPayload), "utf8");
              retryAttempt.htmlBytes = Buffer.byteLength(retryResult.html, "utf8");
              await updateInterface(docId, titleToUpdate, buildPayloadMarkdownField(doc), retryResult.html, request);
              html = retryResult.html;
              mermaidFailed = retryResult.mermaidFailed;
              diagramFailed = retryResult.diagramFailed;
              diagramMetrics = retryResult.diagramMetrics;
              appliedMode = retryMode;
              effectiveOptions = retryOptions;
              contentHash = buildDocsSyncHash(doc, effectiveOptions);
              resolved = true;
              break;
            } catch (retryError) {
              retryAttempt.message =
                retryError instanceof Error ? retryError.message : String(retryError);
            }
          }
          if (!resolved) {
            throw new Error(buildDocsSyncPayloadTooLargeMessage(relName, preview, error, attempts));
          }
        } else {
          throw error;
        }
      }
    }
    if (appliedMode === "default") {
      if (mapping.file_render_modes) {
        delete mapping.file_render_modes[relName];
      }
    } else {
      if (!mapping.file_render_modes || typeof mapping.file_render_modes !== "object") {
        mapping.file_render_modes = {};
      }
      mapping.file_render_modes[relName] = appliedMode;
    }
    if (docId && !mermaidFailed && !diagramFailed) {
      mapping.file_hashes[relName] = contentHash;
    }
    if (action !== "skip") {
      updated += 1;
    }
  }

  return { updated, created, skipped, previewOnly, files: fileInfos, previews };
}

// --- project info / env helpers ---

async function fetchProjectInfo(
  projectId: number,
  baseUrl: string,
  request: YapiRequest,
): Promise<DocsSyncProjectInfo | null> {
  if (!projectId) return null;
  const resp = await request("/api/project/get", "GET", { id: projectId });
  if (resp?.errcode !== 0) {
    throw new Error(`project get failed: ${resp?.errmsg || "unknown error"}`);
  }
  const data = resp?.data || {};
  const name = typeof data.name === "string" ? data.name : "";
  const basepath = typeof data.basepath === "string" ? data.basepath : "";
  const envs = normalizeProjectEnvs(data.env || data.envs);
  return {
    project_id: Number(projectId),
    name,
    basepath,
    envs,
    base_url: baseUrl,
  };
}

function buildEnvUrls(
  projectInfo: DocsSyncProjectInfo | null,
  apiPath: string,
): Record<string, string> {
  const urls: Record<string, string> = {};
  if (!projectInfo || !projectInfo.envs || !projectInfo.envs.length) return urls;
  const basepath = projectInfo.basepath || "";
  projectInfo.envs.forEach((env, index) => {
    const rawKey = env.name || env.domain || `env${index + 1}`;
    const url = buildEnvUrl(env.domain, basepath, apiPath);
    if (!url) return;
    let key = rawKey;
    let suffix = 1;
    while (urls[key]) {
      suffix += 1;
      key = `${rawKey}-${suffix}`;
    }
    urls[key] = url;
  });
  return urls;
}

async function updateDocsSyncCaches(
  homeDir: string,
  baseUrl: string,
  bindingResults: Record<
    string,
    { binding: DocsSyncBinding; files: Record<string, DocsSyncFileInfo> }
  >,
  request: YapiRequest,
): Promise<void> {
  const linksConfig = loadDocsSyncLinks(homeDir);
  const projectsConfig = loadDocsSyncProjects(homeDir);
  const deploymentsConfig = loadDocsSyncDeployments(homeDir);
  const projectCache = new Map<number, DocsSyncProjectInfo | null>();

  const resolveProjectInfo = async (projectId: number): Promise<DocsSyncProjectInfo | null> => {
    if (!projectId) return null;
    if (projectCache.has(projectId)) return projectCache.get(projectId) || null;
    const existing = projectsConfig.projects[String(projectId)] || null;
    try {
      const fresh = await fetchProjectInfo(projectId, baseUrl, request);
      if (fresh) {
        projectsConfig.projects[String(projectId)] = fresh;
        projectCache.set(projectId, fresh);
        return fresh;
      }
    } catch (error) {
      const message = error instanceof Error ? error.message : String(error);
      console.warn(`project get failed (project_id=${projectId}): ${message}`);
    }
    projectCache.set(projectId, existing);
    return existing;
  };

  for (const [name, result] of Object.entries(bindingResults)) {
    const binding = result.binding;
    const projectId = Number(binding.project_id || 0);

    const linkFiles: Record<string, DocsSyncLinkEntry> = {};
    const deploymentFiles: Record<string, DocsSyncDeploymentEntry> = {};
    const projectInfo = await resolveProjectInfo(projectId);

    for (const [fileName, fileInfo] of Object.entries(result.files)) {
      const docUrl = projectId ? buildInterfaceWebUrl(baseUrl, projectId, fileInfo.docId) : "";
      linkFiles[fileName] = {
        doc_id: fileInfo.docId,
        api_path: fileInfo.apiPath,
        url: docUrl,
      };

      deploymentFiles[fileName] = {
        api_path: fileInfo.apiPath,
        env_urls: buildEnvUrls(projectInfo, fileInfo.apiPath),
      };
    }

    linksConfig.bindings[name] = {
      dir: binding.dir,
      project_id: projectId || undefined,
      catid: binding.catid,
      files: linkFiles,
    };

    deploymentsConfig.bindings[name] = {
      dir: binding.dir,
      project_id: projectId || undefined,
      files: deploymentFiles,
    };
  }

  saveDocsSyncLinks(homeDir, linksConfig);
  saveDocsSyncProjects(homeDir, projectsConfig);
  saveDocsSyncDeployments(homeDir, deploymentsConfig);
}

// --- bind subcommand ---

export async function runDocsSyncBindings(action: string, args: DocsSyncBindArgs): Promise<number> {
  if (args.help) {
    // help is handled by yargs
    return 0;
  }

  const writeActions = new Set(["add", "update", "remove", "rm", "delete", "del"]);
  const readActions = new Set(["list", "get", "show"]);
  if (!writeActions.has(action) && !readActions.has(action)) {
    console.error(`unknown docs-sync bind action: ${action}`);
    return 2;
  }

  const homeDir = resolveDocsSyncHome(process.cwd(), writeActions.has(action));
  if (!homeDir) {
    if (action === "list") {
      console.log("no docs-sync bindings (missing .yapi/docs-sync.json)");
      return 0;
    }
    console.error(
      "missing .yapi/docs-sync.json (run in project root or create one with docs-sync bind add)",
    );
    return 2;
  }

  const rootDir = path.dirname(homeDir);
  const config = loadDocsSyncConfig(homeDir);

  if (action === "list") {
    const entries = Object.entries(config.bindings).sort(([a], [b]) => a.localeCompare(b));
    if (!entries.length) {
      console.log("no docs-sync bindings");
      return 0;
    }
    for (const [name, binding] of entries) {
      const filesCount = binding.files ? Object.keys(binding.files).length : 0;
      console.log(
        `${name} dir=${binding.dir} project_id=${binding.project_id ?? ""} catid=${binding.catid ?? ""}` +
          (binding.template_id ? ` template_id=${binding.template_id}` : "") +
          ` files=${filesCount}`,
      );
    }
    return 0;
  }

  if (!args.name) {
    console.error("missing --name for docs-sync bind action");
    return 2;
  }

  if (action === "get" || action === "show") {
    const binding = config.bindings[args.name];
    if (!binding) {
      console.error(`binding not found: ${args.name}`);
      return 2;
    }
    const fileDocIds = binding.files && typeof binding.files === "object" ? binding.files : {};
    const fileHashes =
      binding.file_hashes && typeof binding.file_hashes === "object" ? binding.file_hashes : {};
    const fileLastSyncedAt =
      (binding as DocsSyncBinding & { file_last_synced_at?: Record<string, string> })
        .file_last_synced_at || {};
    const fileNames = Array.from(new Set([...Object.keys(fileDocIds), ...Object.keys(fileHashes)]))
      .sort((a, b) => a.localeCompare(b));
    const files = fileNames.reduce<Record<string, Record<string, string | number>>>(
      (result, fileName) => {
        const entry: Record<string, string | number> = {};
        if (typeof fileHashes[fileName] === "string") {
          entry.hash = fileHashes[fileName];
        }
        if (typeof fileDocIds[fileName] === "number") {
          entry.doc_id = fileDocIds[fileName];
        }
        if (typeof fileLastSyncedAt[fileName] === "string") {
          entry.last_synced_at = fileLastSyncedAt[fileName];
        }
        result[fileName] = entry;
        return result;
      },
      {},
    );
    const payload: Record<string, unknown> = {
      name: args.name,
      dir: binding.dir,
      project_id: binding.project_id,
      catid: binding.catid,
      files,
    };
    if (binding.template_id !== undefined) {
      payload.template_id = binding.template_id;
    }
    console.log(JSON.stringify(payload, null, 2));
    console.log("summary:");
    console.log(`binding ${args.name} -> dir=${binding.dir}`);
    console.log(
      `project_id=${binding.project_id ?? ""} catid=${binding.catid ?? ""}` +
        (binding.template_id !== undefined ? ` template_id=${binding.template_id}` : ""),
    );
    console.log(`files=${fileNames.length}`);
    return 0;
  }

  if (action === "remove" || action === "rm" || action === "delete" || action === "del") {
    if (!config.bindings[args.name]) {
      console.error(`binding not found: ${args.name}`);
      return 2;
    }
    delete config.bindings[args.name];
    saveDocsSyncConfig(homeDir, config);
    console.log(`binding removed: ${args.name}`);
    return 0;
  }

  const hasExisting = Boolean(config.bindings[args.name]);
  if (action === "add" && hasExisting) {
    console.error(`binding already exists: ${args.name}`);
    return 2;
  }
  if (action === "update" && !hasExisting) {
    console.error(`binding not found: ${args.name}`);
    return 2;
  }

  if (action === "add") {
    if (
      !args.dir ||
      !Number.isFinite(args.projectId ?? NaN) ||
      !Number.isFinite(args.catId ?? NaN)
    ) {
      console.error("add requires --dir, --project-id, and --catid");
      return 2;
    }
  }

  const existing = config.bindings[args.name] || ({} as DocsSyncBinding);
  const next: DocsSyncBinding = {
    dir: existing.dir || "",
    project_id: existing.project_id,
    catid: existing.catid,
    template_id: existing.template_id,
    source_files: existing.source_files ? [...existing.source_files] : undefined,
    files: existing.files ? { ...existing.files } : {},
    file_hashes: existing.file_hashes ? { ...existing.file_hashes } : {},
  };

  if (args.dir) {
    next.dir = normalizeBindingDirForContext(homeDir, rootDir, process.cwd(), args.dir);
  }
  if (args.projectId !== undefined && Number.isFinite(args.projectId)) {
    next.project_id = Number(args.projectId);
  }
  if (args.catId !== undefined && Number.isFinite(args.catId)) {
    next.catid = Number(args.catId);
  }
  if (args.templateId !== undefined && Number.isFinite(args.templateId)) {
    next.template_id = Number(args.templateId);
  }
  if (args.clearSourceFiles) {
    next.source_files = [];
  } else if (args.sourceFiles && args.sourceFiles.length) {
    next.source_files = args.sourceFiles;
  }

  if (!next.dir || !next.project_id || !next.catid) {
    console.error("binding requires dir/project_id/catid");
    return 2;
  }

  config.bindings[args.name] = next;
  saveDocsSyncConfig(homeDir, config);
  const resolvedDir = resolveBindingDirForContext(homeDir, rootDir, process.cwd(), next.dir);
  console.log(`${action === "add" ? "binding added" : "binding updated"}: ${args.name}`);
  console.log(`stored_dir=${next.dir}`);
  console.log(`resolved_dir=${resolvedDir}`);
  const gitRoot = findGitRoot(process.cwd());
  if (gitRoot) {
    console.log(`git_root=${gitRoot}`);
  } else if (isGlobalDocsSyncHome(homeDir)) {
    console.warn("warning: no git root detected, relative --dir was resolved from current working directory");
  }
  return 0;
}

// --- main docs-sync command ---

function resolveWatchTargets(options: DocsSyncOptions): string[] {
  const targets = new Set<string>();
  for (const dir of options.dirs || []) {
    const resolved = path.resolve(dir);
    if (fs.existsSync(resolved) && fs.statSync(resolved).isDirectory()) {
      targets.add(resolved);
    }
  }
  if (options.bindings && options.bindings.length > 0) {
    const home = resolveDocsSyncHome(process.cwd(), false);
    if (home) {
      const config = loadDocsSyncConfig(home);
      const rootDir = path.dirname(home);
      for (const name of options.bindings) {
        const binding = config.bindings[name];
        if (!binding) continue;
        const dirPath = resolveBindingDirForContext(home, rootDir, process.cwd(), binding.dir);
        if (fs.existsSync(dirPath) && fs.statSync(dirPath).isDirectory()) {
          targets.add(dirPath);
        }
      }
    }
  }
  return Array.from(targets);
}

export async function runDocsSyncWatch(options: DocsSyncOptions): Promise<number> {
  const debounceMs =
    typeof options.watchDebounceMs === "number" && options.watchDebounceMs > 0
      ? options.watchDebounceMs
      : 500;
  const innerOptions: DocsSyncOptions = { ...options, watch: false };

  console.log("[watch] running initial sync...");
  let initialCode = 0;
  try {
    initialCode = await runDocsSync(innerOptions);
  } catch (error) {
    console.error(
      "[watch] initial sync threw:",
      error instanceof Error ? error.message : String(error),
    );
    initialCode = 2;
  }
  if (initialCode !== 0) {
    console.warn(`[watch] initial sync exited with ${initialCode}; continuing to watch anyway`);
  }

  const targets = resolveWatchTargets(options);
  if (targets.length === 0) {
    console.error(
      "[watch] no directories to watch (use --dir <path> or --binding <name>); exiting",
    );
    return 2;
  }

  console.log(`[watch] watching ${targets.length} dir(s):`);
  for (const t of targets) console.log(`  - ${t}`);
  console.log(`[watch] debounce=${debounceMs}ms (Ctrl+C to stop)`);

  let pending: NodeJS.Timeout | null = null;
  let syncing = false;
  const triggerSync = (): void => {
    if (pending) clearTimeout(pending);
    pending = setTimeout(async () => {
      pending = null;
      if (syncing) {
        triggerSync();
        return;
      }
      syncing = true;
      const ts = new Date().toISOString();
      console.log(`[watch] [${ts}] syncing...`);
      try {
        const code = await runDocsSync(innerOptions);
        console.log(`[watch] sync ${code === 0 ? "ok" : `exited ${code}`}`);
      } catch (error) {
        console.error(
          "[watch] sync threw:",
          error instanceof Error ? error.message : String(error),
        );
      } finally {
        syncing = false;
      }
    }, debounceMs);
  };

  const watchers: fs.FSWatcher[] = [];
  for (const target of targets) {
    try {
      const watcher = fs.watch(target, { recursive: true }, (_event, filename) => {
        if (!filename) return;
        const name = String(filename);
        if (!name.endsWith(".md") && !name.endsWith(".html")) return;
        console.log(`[watch] change detected: ${name}`);
        triggerSync();
      });
      watchers.push(watcher);
    } catch (error) {
      console.error(
        `[watch] failed to watch ${target}:`,
        error instanceof Error ? error.message : String(error),
      );
    }
  }
  if (watchers.length === 0) {
    console.error("[watch] no watchers could be installed; exiting");
    return 2;
  }

  return await new Promise<number>((resolve) => {
    const cleanup = (): void => {
      console.log("\n[watch] stopping...");
      for (const w of watchers) {
        try {
          w.close();
        } catch {
          /* ignore */
        }
      }
      if (pending) clearTimeout(pending);
      resolve(0);
    };
    process.once("SIGINT", cleanup);
    process.once("SIGTERM", cleanup);
  });
}

export async function runDocsSync(options: DocsSyncOptions): Promise<number> {
  if (options.watch) {
    return await runDocsSyncWatch(options);
  }
  try {
    if (!isPandocAvailable()) {
      console.warn("pandoc not found, fallback to markdown-it renderer.");
      console.warn("Install pandoc (macOS): brew install pandoc");
      console.warn("More info: https://pandoc.org/installing.html");
    }
    if (!options.noMermaid && !isMmdcAvailable()) {
      console.warn("mmdc not found, Mermaid blocks will stay as code.");
      console.warn("Install mermaid-cli: npm i -g @mermaid-js/mermaid-cli");
    } else if (!options.noMermaid) {
      const mermaidRuntime = probeMermaidRuntime({
        look: options.mermaidLook,
        handDrawnSeed: options.mermaidHandDrawnSeed,
      });
      if (!mermaidRuntime.ok) {
        console.warn(`mermaid runtime check failed: ${mermaidRuntime.message}`);
      }
    }
    if (!isPlantUmlAvailable()) {
      console.warn("plantuml not found, PlantUML blocks will be removed from HTML.");
      console.warn("Install PlantUML (macOS): brew install plantuml");
    }
    if (!isGraphvizAvailable()) {
      console.warn("graphviz (dot) not found, Graphviz blocks will be removed from HTML.");
      console.warn("Install Graphviz (macOS): brew install graphviz");
    }
    if (!isD2Available()) {
      console.warn("d2 not found, D2 blocks will be removed from HTML.");
      console.warn("Install D2 (macOS): brew install d2");
    }

    if (options.bindings.length && options.dirs.length) {
      console.error("use --binding or --dir, not both");
      return 2;
    }

    const docsSyncHome = resolveDocsSyncHome(process.cwd(), false);
    const docsSyncConfig = docsSyncHome ? loadDocsSyncConfig(docsSyncHome) : null;
    let bindingNames = options.bindings;
    let useBindings = bindingNames.length > 0;
    if (
      !useBindings &&
      !options.dirs.length &&
      docsSyncConfig &&
      Object.keys(docsSyncConfig.bindings).length
    ) {
      useBindings = true;
      bindingNames = Object.keys(docsSyncConfig.bindings);
    }

    if (useBindings && (!docsSyncHome || !docsSyncConfig)) {
      console.error("missing .yapi/docs-sync.json (run docs-sync bind add or use --dir)");
      return 2;
    }
    if (useBindings && !bindingNames.length) {
      console.error("no docs-sync bindings found (run docs-sync bind add or use --dir)");
      return 2;
    }

    const usingDefaultDir = !useBindings && !options.dirs.length;
    const dirs = useBindings ? [] : options.dirs.length ? options.dirs : ["docs/release-notes"];
    if (!useBindings) {
      const missingDirs = dirs.filter((dir) => {
        const dirPath = path.resolve(dir);
        return !fs.existsSync(dirPath) || !fs.statSync(dirPath).isDirectory();
      });
      if (missingDirs.length) {
        const firstMissing = path.resolve(missingDirs[0]);
        console.error(`dir not found: ${firstMissing}`);
        if (usingDefaultDir) {
          const suggestion = suggestDocsSyncDir(process.cwd());
          if (suggestion) {
            console.error(`hint: pass --dir ${suggestion}`);
          }
          console.error("hint: or bind a directory with yapi docs-sync bind add");
        } else {
          console.error("hint: pass --dir <existing_dir> or bind a directory");
        }
        return 2;
      }
    }

    let config: Record<string, string> = {};
    let configPath = options.config || "";
    if (options.config) {
      if (fs.existsSync(configPath)) {
        config = parseSimpleToml(fs.readFileSync(configPath, "utf8"));
      } else {
        const init = await initConfigIfMissing(options);
        if (init) {
          config = init.config;
          configPath = init.configPath;
        } else {
          console.error(`missing config file: ${configPath}`);
          return 2;
        }
      }
    } else {
      const globalPath = globalConfigPath();
      if (fs.existsSync(globalPath)) {
        configPath = globalPath;
        config = parseSimpleToml(fs.readFileSync(globalPath, "utf8"));
      } else {
        const init = await initConfigIfMissing(options);
        if (init) {
          config = init.config;
          configPath = init.configPath;
        } else {
          console.error("missing config: create ~/.yapi/config.toml or pass --config");
          return 2;
        }
      }
    }

    const baseUrl = options.baseUrl || config.base_url || "";
    if (!baseUrl) {
      console.error("missing --base-url or config base_url");
      return 2;
    }

    const projectId = options.projectId || config.project_id || "";
    const rawToken = options.token || config.token || "";
    const token = resolveToken(rawToken, projectId);

    let authMode = (options.authMode || config.auth_mode || "").trim().toLowerCase();
    if (!authMode) {
      authMode = token
        ? "token"
        : options.email || options.password || config.email || config.password
          ? "global"
          : "token";
    }
    if (authMode !== "token" && authMode !== "global") {
      console.error("invalid --auth-mode (use token or global)");
      return 2;
    }

    const headers: Record<string, string> = {};
    const email = options.email || config.email || "";
    const password = options.password || config.password || "";
    const authService =
      authMode === "global"
        ? new YApiAuthService(baseUrl, email || "", password || "", "warn", {
            timeoutMs: options.timeout || 30000,
          })
        : null;
    const canRelogin =
      authMode === "global" &&
      Boolean(authService) &&
      Boolean(email) &&
      Boolean(password) &&
      !options.cookie;

    if (options.cookie) {
      headers.Cookie = options.cookie;
    } else if (authMode === "global") {
      const cachedCookie = authService?.getCachedCookieHeader();
      if (cachedCookie) {
        headers.Cookie = cachedCookie;
      } else if (email && password && authService) {
        try {
          headers.Cookie = await authService.getCookieHeaderWithLogin();
        } catch (error) {
          console.error(error instanceof Error ? error.message : String(error));
          return 2;
        }
      } else {
        console.error("missing email/password for global auth");
        return 2;
      }
    }

    const request: YapiRequest = async (endpoint, method, query = {}, data) => {
      const queryItems: [string, string][] = [];
      for (const [key, value] of Object.entries(query)) {
        if (value !== undefined && value !== null) {
          queryItems.push([key, String(value)]);
        }
      }
      const url = buildUrl(
        baseUrl,
        endpoint,
        queryItems,
        authMode === "token" ? token : "",
        options.tokenParam || "token",
      );

      let body: string | undefined;
      if (data !== undefined) {
        body = JSON.stringify(data);
      }

      const sendOnce = async () => {
        const requestHeaders: Record<string, string> = { ...headers };
        if (body !== undefined) {
          requestHeaders["Content-Type"] = "application/json;charset=UTF-8";
        }
        const response = await fetchWithTimeout(
          url,
          {
            method,
            headers: requestHeaders,
            body,
          },
          options.timeout || 30000,
        );
        const text = await response.text();
        return { response, text, json: parseJsonMaybe(text) };
      };

      let result = await sendOnce();
      if (canRelogin && looksLikeAuthError(result.response.status, result.json)) {
        try {
          headers.Cookie = await authService!.getCookieHeaderWithLogin({ forceLogin: true });
        } catch (error) {
          throw new Error(error instanceof Error ? error.message : String(error));
        }
        result = await sendOnce();
      }

      if (!result.response.ok) {
        throw new HttpStatusError(
          endpoint,
          result.response.status,
          result.response.statusText,
          result.text,
        );
      }
      if (!result.json) {
        throw new Error(`invalid JSON response from ${endpoint}`);
      }
      return result.json;
    };

    if (useBindings) {
      const rootDir = path.dirname(docsSyncHome!);
      const configForBindings = docsSyncConfig!;

      const dirToBindings = new Map<string, string[]>();
      for (const name of bindingNames) {
        const binding = configForBindings.bindings[name];
        if (!binding) {
          throw new Error(`binding not found: ${name}`);
        }
        const dirPath = resolveBindingDirForContext(
          docsSyncHome!,
          rootDir,
          process.cwd(),
          binding.dir,
        );
        const existing = dirToBindings.get(dirPath) || [];
        existing.push(name);
        dirToBindings.set(dirPath, existing);
      }
      const duplicates = Array.from(dirToBindings.entries()).filter(
        ([, names]) => names.length > 1,
      );
      if (duplicates.length) {
        const lines: string[] = [];
        lines.push("invalid docs-sync bindings: multiple bindings share the same dir");
        duplicates.forEach(([dirPath, names]) => {
          lines.push(`- dir=${dirPath} bindings=${names.join(", ")}`);
        });
        lines.push("");
        lines.push("Fix: split docs into separate directories (recommended).");
        lines.push("Example:");
        lines.push(
          "  yapi docs-sync bind update --name <bindingA> --dir docs/yapi-sync/<bindingA>",
        );
        lines.push(
          "  yapi docs-sync bind update --name <bindingB> --dir docs/yapi-sync/<bindingB>",
        );
        throw new Error(lines.join("\n"));
      }

      const bindingResults: Record<
        string,
        { binding: DocsSyncBinding; files: Record<string, DocsSyncFileInfo> }
      > = {};
      for (const name of bindingNames) {
        const binding = configForBindings.bindings[name];
        if (!binding) {
          throw new Error(`binding not found: ${name}`);
        }
        const dirPath = resolveBindingDirForContext(
          docsSyncHome!,
          rootDir,
          process.cwd(),
          binding.dir,
        );
        if (!fs.existsSync(dirPath) || !fs.statSync(dirPath).isDirectory()) {
          const scanRoot = findGitRoot(process.cwd()) || process.cwd();
          throw new Error(buildMissingBindingDirMessage(name, binding.dir, binding, scanRoot));
        }

        const runtimeBinding = cloneMappingForSync(binding, options.sourceFiles);
        const result = await syncDocsDir(dirPath, runtimeBinding, options, request);
        if (options.dryRun) {
          console.log(`dry-run preview binding=${name}`);
          result.previews.forEach((item) => console.log(buildDocsSyncPreviewLine(item)));
        }
        console.log(
          `synced=${result.updated} created=${result.created} skipped=${result.skipped} preview_only=${result.previewOnly} binding=${name} dir=${dirPath}`,
        );
        mergeSyncState(binding, runtimeBinding);
        bindingResults[name] = { binding, files: result.files };
      }

      if (!options.dryRun) {
        saveDocsSyncConfig(docsSyncHome!, configForBindings);
        await updateDocsSyncCaches(docsSyncHome!, baseUrl, bindingResults, request);
      }
    } else {
      for (const dir of dirs) {
        const dirPath = path.resolve(dir);
        if (!fs.existsSync(dirPath) || !fs.statSync(dirPath).isDirectory()) {
          throw new Error(`dir not found: ${dirPath}`);
        }

        const { mapping, mappingPath } = loadMapping(dirPath);
        const runtimeMapping = cloneMappingForSync(mapping, options.sourceFiles);
        const result = await syncDocsDir(dirPath, runtimeMapping, options, request);
        if (options.dryRun) {
          console.log(`dry-run preview dir=${dirPath}`);
          result.previews.forEach((item) => console.log(buildDocsSyncPreviewLine(item)));
        }

        if (!options.dryRun) {
          mergeSyncState(mapping, runtimeMapping);
          saveMapping(mapping, mappingPath);
        }

        console.log(
          `synced=${result.updated} created=${result.created} skipped=${result.skipped} preview_only=${result.previewOnly} dir=${dirPath}`,
        );
      }
    }

    return 0;
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    return 2;
  }
}
